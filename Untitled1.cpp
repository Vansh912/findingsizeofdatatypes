#include<iostream>
#include<string>
using namespace std;

int main(void)
{
	int x;
	float y;
	char z;
	double n;
	
	cout<<"size of int : "<<sizeof(x)<<endl;
	cout<<"size of float : "<<sizeof(y)<<endl;
	cout<<"size of char : "<<sizeof(z)<<endl;
	cout<<"size of double : "<<sizeof(n)<<endl;
	
	return 0;
}
